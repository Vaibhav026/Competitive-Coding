ll prod = 1;

ll maxval = 0;
// A O(log n) function returning primefactorization 
// by dividing by smallest prime factor at every step 
void getFactorization(int x) 
{ 
     
    while (x != 1) 
    {    
        //cout << x << endl;   
        if( m.find(spf[x]) == m.end()){
            m.insert(make_pair(spf[x],1));
            prod = prod*spf[x];

            if(maxval == 0)
                maxval++;
        }
        else{
            m[spf[x]]++;

            if(m[spf[x]] > maxval){
                maxval = m[spf[x]];
            }   
        }
 
        x = x / spf[x]; 
    }
} 
